#include <stdio.h>

double p[128][128],a[128],b[128];
int main(){
	int i,j,k,l,
		n,m,gap,ans
		;
	double *pre,*next,*temp;
	pre=a;
	next=b;
	while(scanf("%d",&n)==1){
		if(n==-1){
			break;
		}
		m=1<<n;
		for(i=0;i<m;++i){
			pre[i]=1.0;
			for(j=0;j<m;++j){
				scanf("%lf",&p[i][j]);
			}
		}
		for(i=0,gap=1;i<n;++i){
			for(j=0;j<m;j+=2*gap){
				for(k=j;k<j+2*gap;++k){
					next[k]=0;
				}
				for(k=j;k<j+gap;++k){
					for(l=j+gap;l<j+2*gap;++l){
						next[k]+=pre[k]*pre[l]*p[k][l];
						next[l]+=pre[k]*pre[l]*p[l][k];
					}
				}
			}
			temp=next;
			next=pre;
			pre=temp;
			gap<<=1;
		}
		ans=0;
		for(i=0;i<m;++i){
			if(pre[i]>pre[ans]){
				ans=i;
			}
		}
		printf("%d\n",ans+1);
	}
	return 0;
}
