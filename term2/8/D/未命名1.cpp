#include <stdio.h>
long long dp[22501]={},p,q;
int weight[100]={};
int main(){
	int i,j,k,n,sum=0,m;
	scanf("%d",&n);
	for(i=0;i<n;++i){
		scanf("%d",weight+i);
	}
	for(i=0,sum=0;i<n;++i){
		sum+=weight[i];
	}
	m=sum>>1;
	dp[0]=1;
	for(i=0;i<n;++i){
		for(j=m;j>=weight[i];--j){
			if(dp[j-weight[i]]){
				dp[j]|=(dp[j-weight[i]]<<1);
			}
		}
	}
	j=n>>1;
	k=(n+1)>>1;
	for(i=0,p=q=1;i<j;++i){
		p<<=1;
		q<<=1;
	}
	if(n&1){
		q<<=1;
	}
	for(i=m;i>0;--i){
		if((dp[i]&p)||(dp[i]&q)){
			printf("%d",i);
			putchar(' ');
			printf("%d",sum-i);
			return 0;
		}
	}
}
